/*
 * limits.h -- implementation-defined integer limits
 */


#ifndef _LIMITS_H_
#define _LIMITS_H_


#define CHAR_BIT	8		/* bits in a char */
#define CHAR_MAX	(+127)		/* maximum value of char */
#define CHAR_MIN	(-127)		/* minimum value of char */
#define INT_MAX		(+2147483647)	/* maximum value of int */
#define INT_MIN		(-2147483647)	/* minimum value of int */
#define LONG_MAX	(+2147483647)	/* maximum value of long */
#define LONG_MIN	(-2147483647)	/* minimum value of long */
#define SCHAR_MAX	(+127)		/* maximum value of signed char */
#define SCHAR_MIN	(-127)		/* minimum value of signed char */
#define SHRT_MAX	(+32767)	/* maximum value of short */
#define SHRT_MIN	(-32767)	/* minimum value of short */
#define UCHAR_MAX	255		/* maximum value of unsigned char */
#define UINT_MAX	4294967295	/* maximum value of unsigned int */
#define ULONG_MAX	4294967295	/* maximum value of unsigned long */
#define USHRT_MAX	65535		/* maximum value of unsigned short */


#endif /* _LIMITS_H_ */
